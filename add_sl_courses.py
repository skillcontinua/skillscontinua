     'They collect in Leones at the day\'s exchange rate.\n'
     'This protects them from the Leone losing value between purchase and sale.\n'
     'Works well for: Professionals (doctors, lawyers, consultants), service businesses, landlords.\n'
     'Less practical for: Market traders with small daily transactions.\n\n'
     'STRATEGY 2 — FAST STOCK TURNOVER:\n'
     'The longer you hold stock before selling, the more inflation erodes your margin.\n'
     'Turn stock over quickly. Do not hold large quantities for long periods.\n'
     'Buy frequently in smaller quantities at current prices rather than buying large stocks at old prices.\n\n'
     'STRATEGY 3 — HOLD STOCK, NOT CASH:\n'
     'During high inflation, cash in Leones loses value daily.\n'
