import os, django
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'skillscontinua_core.settings')
django.setup()

from courses.models import Category, Course, Lesson
from accounts.models import User

admin = User.objects.filter(is_superuser=True).first()

# Get or create Sierra Leone specific category
sl_cat, _ = Category.objects.get_or_create(
    name='Sierra Leone — Local Knowledge',
    defaults={'pillar': 'entrepreneurship', 'language': 'English'}
)

voc_cat = Category.objects.filter(pillar='vocational').first()
fin_cat = Category.objects.filter(pillar='financial').first()

def make_course(title, description, category, level='beginner', hours=25):
    course, created = Course.objects.get_or_create(
        title=title,
        defaults={
            'category': category,
            'description': description,
            'level': level,
            'age_group': 'adult',
            'language': 'English',
            'duration_hours': hours,
            'is_compulsory': False,
            'is_active': True,
            'created_by': admin,
        }
    )
    status = 'CREATED' if created else 'EXISTS'
    print(f'{status}: {title}')
    return course

def add_lessons(course, lessons_data):
    Lesson.objects.filter(course=course).delete()
    for order, title, duration, content in lessons_data:
        Lesson.objects.create(
            course=course, title=title, content_type='text',
            content_text=content, order=order,
            duration_minutes=duration, is_active=True
        )
    print(f'  → {course.lessons.count()} lessons added')

