     'THE REPLACEMENT COST PRINCIPLE:\n'
     'The relevant question is not: What did I pay for this stock?\n'
     'The relevant question is: What will it cost me to REPLACE this stock?\n'
     'Your selling price must cover the REPLACEMENT cost, not just the original cost.\n\n'
     'EXAMPLE:\n'
     'You buy rice at Le 180,000 per bag in January.\n'
     'By March when you have sold it, the price has risen to Le 210,000 per bag.\n'
     'If you priced based on your January cost, you sold too cheaply.\n'
     'You cannot maintain your stock levels.\n'
     'Price based on what it will cost to replace the stock, not what you paid.\n\n'
     'ADJUSTING PRICES FOR INFLATION:\n'
     'Monitor your supplier prices every time you restock.\n'
     'Adjust your selling prices as your costs increase.\n'
     'Customers understand price increases when they are explained clearly.\n'
     'Customers who do not understand should be shown: Your rice now costs Le 210,000. I must sell for more.\n\n'
